export class NextJsGenerator extends BaseGenerator {
  constructor(config: ProjectConfig, destPath: string) {
    super(config, destPath);
  }

  async generate(): Promise<GenerationResult> {
    try {
      // Create project structure
      await this.createDirectoryStructure();

      // Generate configuration files
      await this.generatePackageJson();
      await this.generateTsConfig();
      await this.generateNextConfig();
      await this.generatePostCssConfig();

      // Generate app structure
      await this.generateAppStructure();

      // Generate API routes
      await this.generateApiRoutes();

      // Generate auth configuration
      await this.generateAuthConfig();

      // Generate components
      await this.generateComponents();

      // Generate lib utilities
      await this.generateLibFiles();

      // Generate middleware
      await this.generateMiddlewareFile();

      // Generate Drizzle files
      await this.setupDrizzle();

      // Generate shared files (gitignore, prettier, docker, etc.)
      await this.setupSharedFiles();

      // Copy any existing templates
      const templateDir = getTemplateDir('nextjs');
      await this.copyTemplateFiles(templateDir);

      return this.createResult(true, 'Next.js project generated successfully');
    } catch (error) {
      const errorMessage = error instanceof Error ? error.message : 'Unknown error';
      this.errors.push(errorMessage);
      return this.createResult(false, `Failed to generate Next.js project: ${errorMessage}`);
    }
  }

  private async createDirectoryStructure(): Promise<void> {
    const directories = [
      'app',
      'app/api',
      'app/api/health',
      'app/api/auth',
      'app/auth/signin',
      'app/auth/signup',
      'app/dashboard',
      'components',
      'components/auth',
      'components/ui',
      'lib',
      'src/db',
      'src/db/schema',
      'drizzle/migrations',
      'public',
    ];

    if (this.config.auth === 'lucia') {
      directories.push('app/api/auth/signin', 'app/api/auth/signup', 'app/api/auth/signout');
    } else {
      directories.push('app/api/auth/register');
    }

    const fsExtra = await import('fs-extra');
    for (const dir of directories) {
      const dirPath = path.join(this.destPath, dir);
      await fsExtra.default.ensureDir(dirPath);
    }
  }

  protected async generatePackageJson(): Promise<void> {
    const packageJson = generateNextJsPackageJson(this.config);
    await this.writeProjectJsonFile('package.json', packageJson);
  }

  protected async generateTsConfig(): Promise<void> {
    const tsConfig = generateNextJsTsConfig();
    await this.writeProjectJsonFile('tsconfig.json', tsConfig);
  }

  private async generateNextConfig(): Promise<void> {
    await this.writeProjectFile('next.config.ts', generateNextConfig(this.config));
  }

  private async generatePostCssConfig(): Promise<void> {
    await this.writeProjectFile('postcss.config.mjs', generatePostCssConfig());
  }

  private async generateAppStructure(): Promise<void> {
    // Layout
    await this.writeProjectFile('app/layout.tsx', generateRootLayout(this.config));

    // Home page
    await this.writeProjectFile('app/page.tsx', generateHomePage(this.config));

    // Global styles
    await this.writeProjectFile('app/globals.css', generateGlobalsCss());

    // Loading state
    await this.writeProjectFile('app/loading.tsx', generateLoadingPage());

    // Error boundary
    await this.writeProjectFile('app/error.tsx', generateErrorPage());

    // 404 page
    await this.writeProjectFile('app/not-found.tsx', generateNotFoundPage());

    // Dashboard page
    await this.writeProjectFile('app/dashboard/page.tsx', generateDashboardPage(this.config));

    // Auth pages
    await this.writeProjectFile('app/auth/signin/page.tsx', generateSignInPage(this.config));
    await this.writeProjectFile('app/auth/signup/page.tsx', generateSignUpPage(this.config));
  }

  private async generateApiRoutes(): Promise<void> {
    // Health check route
    await this.writeProjectFile('app/api/health/route.ts', generateHealthRoute());

    if (this.config.auth === 'authjs') {
      // Registration route for Auth.js
      await this.writeProjectFile(
        'app/api/auth/register/route.ts',
        generateRegisterRoute(this.config)
      );

      // Auth.js route handler
      await this.writeProjectFile(
        'app/api/auth/[...nextauth]/route.ts',
        `import { handlers } from "@/lib/auth";
export const { GET, POST } = handlers;
`
      );
    } else {
      // Lucia auth routes
      await this.writeProjectFile(
        'app/api/auth/signin/route.ts',
        generateLuciaSignInRoute(this.config)
      );
      await this.writeProjectFile(
        'app/api/auth/signup/route.ts',
        generateLuciaSignUpRoute(this.config)
      );
      await this.writeProjectFile(
        'app/api/auth/signout/route.ts',
        generateLuciaSignOutRoute()
      );
    }
  }

  private async generateAuthConfig(): Promise<void> {
    if (this.config.auth === 'authjs') {
      await this.writeProjectFile('lib/auth.ts', generateAuthJsConfig(this.config));
    } else {
      await this.writeProjectFile('lib/auth.ts', generateLuciaConfig(this.config));
    }
  }

  private async generateComponents(): Promise<void> {
    // Sign out button
    await this.writeProjectFile(
      'components/auth/sign-out-button.tsx',
      generateSignOutButton(this.config)
    );

    // Example button component
    await this.writeProjectFile(
      'components/ui/button.tsx',
      `import { cn } from "@/lib/utils";
import { forwardRef, type ButtonHTMLAttributes } from "react";

export interface ButtonProps extends ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: "default" | "outline" | "ghost";
  size?: "sm" | "md" | "lg";
}

export const Button = forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant = "default", size = "md", ...props }, ref) => {
    return (
      <button
        ref={ref}
        className={cn(
          "inline-flex items-center justify-center rounded-lg font-medium transition-colors focus:outline-none focus:ring-2 focus:ring-blue-500 focus:ring-offset-2 disabled:opacity-50 disabled:cursor-not-allowed",
          {
            "bg-blue-600 text-white hover:bg-blue-700": variant === "default",
            "border border-gray-300 dark:border-gray-700 hover:bg-gray-100 dark:hover:bg-gray-800":
              variant === "outline",
            "hover:bg-gray-100 dark:hover:bg-gray-800": variant === "ghost",
          },
          {
            "px-3 py-1.5 text-sm": size === "sm",
            "px-4 py-2 text-base": size === "md",
            "px-6 py-3 text-lg": size === "lg",
          },
          className
        )}
        {...props}
      />
    );
  }
);

Button.displayName = "Button";
`
    );
  }

  private async generateLibFiles(): Promise<void> {
    // Utils
    await this.writeProjectFile('lib/utils.ts', generateUtils());
  }

  private async generateMiddlewareFile(): Promise<void> {
    await this.writeProjectFile('middleware.ts', generateMiddleware(this.config));
  }

  protected override async setupDrizzle(): Promise<void> {
    // Override to use correct paths for Next.js
    // Create custom paths for Next.js (db in src/db)
    await writeFile(
      path.join(this.destPath, 'drizzle.config.ts'),
      this.generateNextJsDrizzleConfig()
    );
    this.createdFiles.push(path.join(this.destPath, 'drizzle.config.ts'));

    // Create database client
    await writeFile(
      path.join(this.destPath, 'src', 'db', 'index.ts'),
      this.generateNextJsDbClient()
    );
    this.createdFiles.push(path.join(this.destPath, 'src', 'db', 'index.ts'));

    // Create schema files using base generator
    const { generateUsersSchema, generateSessionsSchema, generateAccountsSchema, generateVerificationTokensSchema, generateSchemaIndex } = await import('./base.js');

    const schemaDir = path.join(this.destPath, 'src', 'db', 'schema');

    await writeFile(path.join(schemaDir, 'users.ts'), generateUsersSchema(this.config));
    await writeFile(path.join(schemaDir, 'sessions.ts'), generateSessionsSchema(this.config));

    if (this.config.auth === 'authjs') {
      await writeFile(path.join(schemaDir, 'accounts.ts'), generateAccountsSchema(this.config));
      await writeFile(path.join(schemaDir, 'verificationTokens.ts'), generateVerificationTokensSchema(this.config));
    }

    await writeFile(path.join(schemaDir, 'index.ts'), generateSchemaIndex(this.config));

    this.createdFiles.push(
      path.join(schemaDir, 'users.ts'),
      path.join(schemaDir, 'sessions.ts'),
      path.join(schemaDir, 'index.ts')
    );

    if (this.config.auth === 'authjs') {
      this.createdFiles.push(
        path.join(schemaDir, 'accounts.ts'),
        path.join(schemaDir, 'verificationTokens.ts')
      );
    }

    // Create migrations directory
    const migrationsDir = path.join(this.destPath, 'drizzle', 'migrations');
    const fsExtra = await import('fs-extra');
    await fsExtra.default.ensureDir(migrationsDir);
    await writeFile(path.join(migrationsDir, '.gitkeep'), '');
  }

  private generateNextJsDrizzleConfig(): string {
    if (this.config.database === 'postgresql') {
      return `import type { Config } from 'drizzle-kit';
import * as dotenv from 'dotenv';

dotenv.config();

if (!process.env.DATABASE_URL) {
  throw new Error('DATABASE_URL environment variable is required');
}

export default {
  schema: './src/db/schema/index.ts',
  out: './drizzle/migrations',
  dialect: 'postgresql',
  dbCredentials: {
    url: process.env.DATABASE_URL,
  },
  verbose: true,
  strict: true,
} satisfies Config;
`;
    }

    return `import type { Config } from 'drizzle-kit';
import * as dotenv from 'dotenv';

dotenv.config();

const dbPath = process.env.DATABASE_PATH || './data/${this.config.name}.db';

export default {
  schema: './src/db/schema/index.ts',
  out: './drizzle/migrations',
  dialect: 'sqlite',
  dbCredentials: {
    url: dbPath,
  },
  verbose: true,
  strict: true,
} satisfies Config;
`;
  }

  private generateNextJsDbClient(): string {
    if (this.config.database === 'postgresql') {
      return `import { drizzle } from 'drizzle-orm/postgres-js';
import postgres from 'postgres';
import * as schema from './schema';

if (!process.env.DATABASE_URL) {
  throw new Error('DATABASE_URL environment variable is required');
}

const connectionString = process.env.DATABASE_URL;

const queryClient = postgres(connectionString, {
  max: 10,
  idle_timeout: 20,
  connect_timeout: 10,
});

export const db = drizzle(queryClient, { schema });

export type Database = typeof db;
`;
    }

    return `import { drizzle } from 'drizzle-orm/better-sqlite3';
import Database from 'better-sqlite3';
import * as schema from './schema';
import path from 'path';
import fs from 'fs';

const dbPath = process.env.DATABASE_PATH || './data/${this.config.name}.db';
const dbDir = path.dirname(dbPath);

if (!fs.existsSync(dbDir)) {
  fs.mkdirSync(dbDir, { recursive: true });
}

const sqlite = new Database(dbPath);
sqlite.pragma('journal_mode = WAL');

export const db = drizzle(sqlite, { schema });

export type Database = typeof db;
`;
  }
}

